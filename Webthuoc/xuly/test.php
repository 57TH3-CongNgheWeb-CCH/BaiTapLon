	<p>Cây hoa atiso một loại trà, cũng là một vị thuốc nam điều trị bệnh gan rất hay. Ở bài viết này xin giới thiệu với các bạn những công dụng chính và cách dùng vị thuốc này hiệu quả nhất.</p>

<p>Cây Atiso được đọc là (ac ti sô) một loại cây thuốc nam được sử dụng bởi người Châu Âu cách đây hàng ngàn năm.</p>

<p>Tên khoa học: Cynara scolymus L. Thuộc họ cúc</p>

<p>Khu vực phân bố</p>

<p>Cây Atiso (ac ti sô) có nguồn gốc từ Địa Trung Hải, đã được người Hy Lạp cổ đại trồng làm rau ăn và làm thuốc. Cây được du nhập vào Việt Nam vào những năm 1930 bởi người Pháp.

Hiện nay cây Atiso (ac ti sô) được trồng nhiều ở Đà Lạt làm dược liệu, cách đây vài năm trà Atiso là một trong những đồ uống khoái khẩu của nhiều người, được dùng như một thứ đồ uống có công dụng mát gan, giải độc cho cơ thể.
</p>
<p>Bộ phận dùng: Toàn cây gồm lá, thân, rễ và cả hoa.</p>



<p>Cách chế biến và thu hái</p>

<p>Có thể dùng tươi hoặc dùng khô, cây tươi dùng làm rau ăn. Ngoài ra cây còn được phơi khô làm trà và dược liệu.

Hoa Atiso thường được thu hái quanh năm, hoa thu về được rửa sạch, phơi hoặc sấy khô làm thuốc. Hiện nay có nhiều công ty đã chế biến dưới dạng trà túi lọc và cao atiso rất tiện sử dụng.</p>

<p>Thành phần hóa học:

Cây Atiso có chứa nhiều hợp chất quý có lợi cho cơ thể như: Các acid hữu cơ, Flavonoid, Cynaopicrin và nhiều loại khoáng chất khác.</p>
