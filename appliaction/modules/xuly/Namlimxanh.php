	<p>Nấm lim xanh có rất nhiều tác dụng, một trong những tác dụng quý nhất của vị thuốc này đó là tác dụng hỗ trợ điều trị bệnh tiểu đường. 

Thành phần nấm lim xanh gồm rất nhiều các loại dược chất không đường, các dược chất này có khả năng chuyển hóa đường huyết trong máu, làm giảm lượng đường huyết giúp bệnh nhân tiểu đường ổn định được đường huyết trong máu.

Ngoài ra trong nấm lim xanh còn có cơ chế phục hồi các tế bào ở tuyến tụy, giúp tăng sản sinh ra Insulin (Insulin là một chất quan trọng trong có thể, thiếu insulin đồng nghĩa với bạn bị bệnh tiểu đường). Cơ chế của nấm lim xanh là gián tiếp sản sinh ra insulin. Đây là 1 cơ chế điều trị tiểu đường bền vững hơn hẳn so với các loại thuốc điều trị tiểu đường có bán trên thị trường hiện nay.</p>

<p>Các phương pháp điều trị bệnh tiểu đường hiện nay đều nhằm mục đích trung là cung cấp trực tiếp 1 lượng insulin cho người bệnh. Ưu điểm của các phương pháp này là hiệu quả nhanh, xong nhược điểm là: Người bệnh tiểu đường phải phụ thuộc hoàn toàn vào thuốc, về lâu về dài làm sẽ làm vô hiệu hóa chức năng tạo insulin tự nhiên của tuyến tụy.</p>

<p>Như vậy nấm lim xanh với bệnh tiểu đường có 2 tác dụng chính sau:</p>

<p>Giảm lượng đường trong máu</p>
<p>Kích thích tăng sản sinh insulin ở tuyến tụy</p>
<p>Tăng cường sức đề kháng, ngăn ngừa biến chứng của bệnh tiểu đường</p>
<p>Đây là 2 tác dụng quý giá nhất của nấm lim xanh trong điều trị tiểu đường, các tác dụng này vượt trội hơn hẳn so với các loại thảo dược được cho là có tác dụng điều trị tiểu đường hiện nay.

Ngoài ra hàng trục dược chất quý trong nấm lim xanh còn có công dụng đào thải độc tố trong cơ thể, giúp người bệnh nâng cao sức đề kháng, ngăn ngừa các biến chứng bệnh tiểu đường.</p>
<p></p>Cách dùng nấm lim xanh điều trị tiểu đường

<p>Chuẩn bị: Nấm lim xanh khô 20g, nước 300ml</p>
<p>Cách làm: Nấm rửa sạch (Nhất là phần chân nấm), thái mỏng, bỏ vào ấm rồi tráng qua 1 lần bằng nước sôi. Đổ 300ml nước sôi vào ấm hãm trong thời gian 30 phút là dùng được.</p>
<p>Cách dùng: Uống trước bữa ăn 30 – 40 phút. Các thời gian dùng thuốc Tây từ 30 phút trở lên.</p>
<p>Lưu ý khi dùng nấm lim xanh điều trị tiểu đường:</p>

<p>Khi dùng nấm các bạn vẫn phải tuân thủ chế độ ăn kiêng của người bệnh tiểu đường
Vận đông, thể dục đều đặn sẽ giúp ích rất nhiều cho việc điều trị bệnh
Không bỏ thuốc Tây mà nên kết hợp sử dụng để đem lại hiệu quả điều trị cao nhất.</p>
