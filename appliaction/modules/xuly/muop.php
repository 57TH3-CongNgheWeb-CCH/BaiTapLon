	<p>Cây mướp đắng rừng còn được gọi là khổ qua rừng là một loài cây mọc hoang ở khắp các sườn đồi từ Bắc trí Nam.

Mướp đắng hiện là một trong những loại thực phẩm được nhiều người ưa thích. Tuy có vị đắng nhưng loại mướp đặc biệt này vẫn rất hấp dẫn người sử dụng bởi nó rất tốt cho sức khỏe, đặc biệt là những bệnh nhân bị huyết áp cao.</p>

<p>Đặc điểm nhận biết mướp đắng rừng và mướp đắng nhà</p>

<p>Mướp đắng rừng có cùng họ với mướp đắng nhà xong có kích thước quả và dây nhỏ hơn, vị đắng cao hơn.

Mướp đắng rừng vẫn được người dân vùng cao hái về dùng làm rau ăn hàng ngày. Cây mọc hoang rất nhiều ở các sườn đồi, đến nỗi nhiều lúc người dân còn phải phát quang đi để tránh làm ảnh hưởng tới những cây trồng khác.

Ngày nay, nhu cầu sử dụng mướp đắng làm thuốc tăng cao, người dân vùng cao đi hái quả về thái xong phơi khô để làm thuốc. Mướp khô có thể dùng hãm với nước sôi hoặc đun nước uống hàng ngày.</p>
<p>Thành phần hóa học: Trong quả có chứa hoạt chất glucozit đắng gọi là momocđixin, đặc biệt trong quả mướp đắng có hàm lượng vitamin B1, C, betain, protein khá cao.</p>

<p>Tác dụng của mướp đắng rừng</p>

<p>Toàn bộ cây khổ qua rừng gồm lá, dây và quả đều được dùng để làm thuốc. Cây có vị đắng, tính mát có một số tác dụng chính như sau:</p>

<p>Tác dụng hạ huyết áp</p>
<p>Tác dụng mát gan giải độc, hạ men gan</p>
<p>Tác dụng ổn định đường huyết, hỗ trợ điều trị bệnh tiểu đường
Cách dùng khổ qua rừng làm thuốc
</p>
<p>Cây mướp đắng rừng có thể dùng tươi hoặc dùng khô đều được.</p>

<p>Dùng tươi: Dùng lá, dây hoặc quả nấu canh ăn hàng ngày như các loại mướp thông thường.</p>
<p>Dùng khô: Lá, quả đem phơi để sử dùng. Liều dùng: 20g/ngày dưới dạng nước sắc hoặc hãm nước sôi uống hàng ngày.</p>
